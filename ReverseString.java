package com.nagarro.interview.practice;

public class ReverseString {

	public static void main(String[] args) {
		String string = "naman";
		String reverse = " ";
		int len = string.length();
		
		for(int i = len-1; i>=0;i--) {
			reverse = reverse+string.charAt(i);
		}
		System.out.println(reverse);

	}}
		
		
		
		
